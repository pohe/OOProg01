using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace OOProg01
{
    public static class MyMath
    {

        public static int Add(int tal1, int tal2)
        {
            int sum = tal1 + tal2;
            return sum;
        }

        public static int Subtract(int tal1, int tal2)
        {
            int subResult = tal1 - tal2;
            return subResult;
        }

        public static int Multiply(int tal1, int tal2)
        {
            int mul = tal1 * tal2;
            return mul;
        }
    }
}
